package es.ubiqua.nhservices.model;

public class HotelDesayuno {
	private int id;
	private int id_hotel;
	private int id_hotel_directory_desayuno;

	public HotelDesayuno(){
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getId_hotel() {
		return id_hotel;
	}

	public void setId_hotel(int id_hotel) {
		this.id_hotel = id_hotel;
	}

	public int getId_hotel_directory_desayuno() {
		return id_hotel_directory_desayuno;
	}

	public void setId_hotel_directory_desayuno(int id_hotel_directory_desayuno) {
		this.id_hotel_directory_desayuno = id_hotel_directory_desayuno;
	}
	
}
