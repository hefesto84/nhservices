package es.ubiqua.nhservices.model;

public class Traducciones {
	
	private String identifier;
	private String ca;
	private String de;
	private String en;
	private String es;
	private String fr;
	private String it;
	private String nl;
	private String pt;
	private String translation;
	
	public String getIdentifier() {
		return identifier;
	}
	
	public void setIdentifier(String identifier) {
		this.identifier = identifier;
	}
	
	public String getCa() {
		return ca;
	}
	
	public void setCa(String ca) {
		this.ca = ca;
	}
	
	public String getDe() {
		return de;
	}
	
	public void setDe(String de) {
		this.de = de;
	}
	
	public String getEn() {
		return en;
	}
	
	public void setEn(String en) {
		this.en = en;
	}
	
	public String getEs() {
		return es;
	}
	
	public void setEs(String es) {
		this.es = es;
	}
	
	public String getFr() {
		return fr;
	}
	
	public void setFr(String fr) {
		this.fr = fr;
	}
	
	public String getIt() {
		return it;
	}
	
	public void setIt(String it) {
		this.it = it;
	}
	
	public String getNl() {
		return nl;
	}
	
	public void setNl(String nl) {
		this.nl = nl;
	}
	
	public String getPt() {
		return pt;
	}
	
	public void setPt(String pt) {
		this.pt = pt;
	}

	public String getTranslation() {
		return translation;
	}

	public void setTranslation(String translation) {
		this.translation = translation;
	}
	
}
