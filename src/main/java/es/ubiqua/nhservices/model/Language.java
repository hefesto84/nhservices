package es.ubiqua.nhservices.model;

public class Language {
	private int id;
	private String code;
	
	public Language(){
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

}
