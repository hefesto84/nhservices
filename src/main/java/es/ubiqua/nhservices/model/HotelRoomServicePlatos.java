package es.ubiqua.nhservices.model;

public class HotelRoomServicePlatos {
	private int id;
	private int id_hotel;
	private int id_room_service_platos;
	
	public int getId() {
		return id;
	}
	
	public int getId_hotel() {
		return id_hotel;
	}
	
	public int getId_room_service_platos() {
		return id_room_service_platos;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public void setId_hotel(int id_hotel) {
		this.id_hotel = id_hotel;
	}
	
	public void setId_room_service_platos(int id_room_service_platos) {
		this.id_room_service_platos = id_room_service_platos;
	}

}
