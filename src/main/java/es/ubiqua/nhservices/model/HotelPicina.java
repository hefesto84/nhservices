package es.ubiqua.nhservices.model;

public class HotelPicina {
	private int id;
	private int id_hotel;
	private int id_hotel_directory_picina;
	
	public HotelPicina(){
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getId_hotel() {
		return id_hotel;
	}

	public void setId_hotel(int id_hotel) {
		this.id_hotel = id_hotel;
	}

	public int getId_hotel_directory_picina() {
		return id_hotel_directory_picina;
	}

	public void setId_hotel_directory_picina(int id_hotel_directory_picina) {
		this.id_hotel_directory_picina = id_hotel_directory_picina;
	}

}
